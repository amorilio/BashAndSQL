/*
table: MSC_PROFILE_CAUSAL_FACTORS
profile causal factors definition table.
*/
DROP TABLE MSC_PROFILE_CAUSAL_FACTORS cascade constraints;
CREATE TABLE MSC_PROFILE_CAUSAL_FACTORS
(
  PLAN_ID         NUMBER(18)          NOT NULL, -- plan_id to indicate the plan we entered from (0 - Profile not assigned to any plan) (FK)
  PROFILE_ID          NUMBER(18)          NOT NULL, -- Profile identifier.
  CAUSAL_FACTOR_ID          NUMBER(18)          NOT NULL, -- Causal Factor identifier
  CAUSAL_TYPE_ID          NUMBER(18)  NOT NULL, -- The Causal type the current Causal belong to (FK)
  CAUSAL_DECOMP_ID          NUMBER(18)  NOT NULL, -- The Causal decomposition the current Causal belong to (FK)
  DEFINING_MEASURE NUMBER(18), -- Measure ID containing the data for the causal factor (FK)
  TRANSPOSE_MEASURE NUMBER(18), -- Measure ID containing information used to transpose a single causal into several causals
  FILL_MISSING_VALUES NUMBER(1), -- indicating  whether null values for the causal factors should be automatically filled based on other values or set to 0. 0-No 1-Yes
  SHORT_METHODS NUMBER(1), -- indicating whether causal factors will be used by forecasting method of type  “Short” 0-No 1-Yes
  LONG_METHODS NUMBER(1), -- indicating whether causal factors will be used by forecasting method of type  “Long” 0-No 1-Yes
  MULTI_METHOD_GR1 NUMBER(1), -- Indicates if causal factor will be used in first causal group for DMULT forecasting method  0-No 1-Yes
  MULTI_METHOD_GR2 NUMBER(1), -- Indicates if causal factor will be used in second causal group for DMULT forecasting method 0-No 1-Yes
  NON_SEASONAL NUMBER(1), -- indicating whether causal factors will be used by forecasting method of type  “Non Seasonal” 0-No 1-Yes
  SCALE_UNEVEN_PERIODS NUMBER(1), -- indicates causal factor will be scaled if forecasting engine determines history and future periods are not of equal length 0-No 1-Yes
  SHAPE_DEF NUMBER(1), -- indicates whether causal will be used during periods indicating shapes 0-No 1-Yes
  CREATED_BY     VARCHAR2(64 BYTE) NOT NULL,
  CREATION_DATE   DATE  NOT NULL,
  LAST_UPDATED_BY    VARCHAR2(64 BYTE) NOT NULL,
  LAST_UPDATE_DATE  DATE   NOT NULL,
  LAST_UPDATE_LOGIN  VARCHAR2(32 BYTE)
);
ALTER TABLE MSC_PROFILE_CAUSAL_FACTORS ADD(CONSTRAINT MSC_PROFILE_CAUSAL_FACTORS_PK PRIMARY KEY (PLAN_ID, PROFILE_ID, CAUSAL_FACTOR_ID));


